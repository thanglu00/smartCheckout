import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:fluttermessage/object_transfer/userInfo.dart';
import 'package:fluttermessage/repository/user_repository.dart';
import 'package:fluttermessage/view/phoneNumberScreen.dart';
import 'package:fluttermessage/view/simple_bloc_delegate.dart';
import 'package:fluttermessage/view/home_screen.dart';
import 'package:fluttermessage/view/splash_screen.dart';

import 'bloc/authentication_bloc/authentication_bloc.dart';
import 'bloc/login/login_screen.dart';

void main() {
  WidgetsFlutterBinding.ensureInitialized();
  BlocSupervisor.delegate = SimpleBlocDelegate();
  final UserRepository userRepository = UserRepository();
  runApp(
    BlocProvider(
      create: (context) => AuthenticationBloc(
        userRepository: userRepository,
      )..add(AuthenticationStarted()),
      child: MyApp(userRepository: userRepository),
    ),
  );
}


class MyApp extends StatelessWidget {
  final UserRepository _userRepository;

  MyApp({Key key, @required UserRepository userRepository})
      : assert(userRepository != null),
        _userRepository = userRepository,
        super(key: key);

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      home: BlocBuilder<AuthenticationBloc, AuthenticationState>(
        builder: (context, state) {
          if (state is AuthenticationFailure) {
            return LoginScreen(userRepository: _userRepository);
          }
          if (state is AuthenticationSuccess) {
            return HomeScreen(User_Info(state.displayName,state.email));
          }
          return SplashScreen();
        },
      ),
    );
  }
}





